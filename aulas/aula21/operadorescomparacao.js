/*    Operadores De Comparação
 *
 *    > Maior que
 *    >= Maior ou igual
 *    < Menor que
 *    <= Menor ou igual
 *
 *    == Igualdade (só valor, não recomendado)
 *    === Igualdade estrita (valor e tipo)
 *    != Diferente (só valor, não recomendado)
 *    !== Diferente estrito (valor e tipo)
 */
console.log(">:", 10 > 5) // Essas espressões sempre retornam valor boolean
const comparacao = 5 > 5 // Pode criar variáveis com operadores
console.log(">:", comparacao)

console.log(" ")

