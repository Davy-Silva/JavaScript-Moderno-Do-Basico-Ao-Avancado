/*  
 *  Operadores Lógicos
 *
 *  Trabalham com valores Booleans
 * 
 *  && / AND
 *  || / OR
 *  ! / NOT
 */

console.log(true && true) // Todas as expressões precisam ser verdadeiras para retornar true
console.log(true && false)

console.log(" ")

console.log(true || false) // Pelo menos uma expressão precisar ser verdadeira para retornar true
console.log(false || false)

console.log(" ")

console.log(!(false)) // Nega o resultado e retorna o resultado invertido
console.log(!(true))